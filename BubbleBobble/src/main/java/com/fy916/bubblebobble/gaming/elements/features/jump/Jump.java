package com.fy916.bubblebobble.gaming.elements.features.jump;

/**
 * An Interface which offers the jump method framework, to be utilized with the object that can jump.
 * This shows the STRATEGY and TEMPLATE Design Pattern. 
 * @author fy916
 * @version 1.0
 */
public interface Jump {

    /**
     * Abstract Method which perform the jump action of the {@link com.fy916.bubblebobble.gaming.elements.movingelements.MovingObject}.
     * @author fy916
     */
    void jump();
}
