package com.fy916.bubblebobble.gaming.elements.features.die;

/**
 * An Interface which offers the die method framework, to be utilized with the object that can die.<br/>
 * This shows the STRATEGY and TEMPLATE Design Pattern. 
 * @author fy916
 * @version 1.0
 */
public interface Die {

    /**
     * Abstract Method which perform the die action of the {@link com.fy916.bubblebobble.gaming.elements.movingelements.MovingObject}
     * @author fy916
     */
    void die();
}
